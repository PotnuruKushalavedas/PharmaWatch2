export const getTranslation = (key, lang = null) => {
  const currentLang = lang || localStorage.getItem('pharmawatch_lang') || 'en';
  const dictionary = translations[currentLang] || translations.en;
  return dictionary[key] || key;
};

export const applyLanguage = (lang) => {
  const dictionary = translations[lang] || translations.en;
  for (const [id, text] of Object.entries(dictionary)) {
    const el = document.getElementById(id);
    if (el) {
      if (el.tagName === 'INPUT' && el.type === 'button') {
        el.value = text;
      } else {
        el.innerText = text;
      }
    }
  }
  // Also update user greeting if visible
  const name = localStorage.getItem('pharmawatch_user_name');
  if (name) {
    const greetingEl = document.getElementById('user-greeting');
    if (greetingEl) {
      if (lang === 'hi') greetingEl.innerText = `नमस्ते, ${name}`;
      else if (lang === 'te') greetingEl.innerText = `నమస్కారం, ${name}`;
      else greetingEl.innerText = `Hello, ${name}`;
    }
  }
};
